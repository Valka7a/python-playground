def check_for_match(male, female):
    # Check for age difference
    if abs(male['age'] - female['age']) > 6:
        return

    # Check for ex boy/girl-friend
    if male['name'] in female['ex'] or female['name'] in male['ex']:
        return

    # Check for mutual interests
    male_interests = set(male['interests'])
    female_interests = set(female['interests'])

    intersection = male_interests.intersection(female_interests)
    if intersection:
        print("{}({}) и {}({}); - общи интереси: {}".format(
            male['name'],
            male['age'],
            female['name'],
            female['age'],
            ', '.join(intersection)
        ))

# Filter the males and females from people list
males = list(filter(lambda person: person['gender'] == 'male', people))
females = list(filter(lambda person: person['gender'] == 'female', people))


for male in males:
    for female in females:
        check_for_match(male, female)
